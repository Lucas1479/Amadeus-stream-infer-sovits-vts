# ================= 核心类定义 =================

class RAGSystem:
    def __init__(self):
        print(f"[{time.strftime('%H:%M:%S')}] 正在初始化 RAG 系统...")

        # 1. 加载嵌入模型 (这一步无论如何都要做，但模型很小，很快)
        # device='cpu' 确保不占用您宝贵的显存
        print(f"[{time.strftime('%H:%M:%S')}] 加载 Embedding 模型: {MODEL_NAME}...")
        self.embedder = SentenceTransformer(MODEL_NAME, device='cpu')

        # 2. 尝试加载现有索引，如果不存在则构建
        if os.path.exists(INDEX_FILE) and os.path.exists(DATA_FILE):
            self.load_from_disk()
        else:
            print(f"[{time.strftime('%H:%M:%S')}] 未找到本地索引，开始从原始数据构建...")
            self.build_from_scratch(RAW_KNOWLEDGE_BASE)

        print(f"[{time.strftime('%H:%M:%S')}] RAG 系统就绪！当前知识库条目数: {self.index.ntotal}")

    def build_from_scratch(self, text_list):
        """首次构建：向量化 -> 建索引 -> 保存磁盘"""
        t0 = time.time()

        # A. 向量化 (最耗时步骤)
        print("   >>> 正在向量化知识库 (首次运行会比较慢)...")
        vectors = self.embedder.encode(text_list, convert_to_numpy=True)

        # B. 建立 FAISS 索引
        vector_dim = vectors.shape[1]
        self.index = faiss.IndexFlatL2(vector_dim)
        self.index.add(vectors)

        # C. 保存数据到内存变量
        self.knowledge_text = text_list

        # D. 持久化到磁盘 (保存)
        print("   >>> 正在保存索引到磁盘...")
        faiss.write_index(self.index, INDEX_FILE)
        with open(DATA_FILE, 'w', encoding='utf-8') as f:
            json.dump(text_list, f, ensure_ascii=False, indent=2)

        print(f"   >>> 构建完成！耗时: {time.time() - t0:.2f}s")

    def load_from_disk(self):
        """后续启动：直接读取磁盘 -> 极速加载"""
        t0 = time.time()

        # A. 读取 FAISS 索引
        self.index = faiss.read_index(INDEX_FILE)

        # B. 读取对应文本
        with open(DATA_FILE, 'r', encoding='utf-8') as f:
            self.knowledge_text = json.load(f)

        print(f"   >>> 已从磁盘加载索引 (跳过向量化)。耗时: {time.time() - t0:.4f}s")

    def search(self, query, k=1, threshold=1.5):
        """运行时检索函数"""
        # 1. 向量化用户问题 (必须实时做)
        # 使用 encode 而不是 print 里的 embedding
        query_vector = self.embedder.encode(["query: " + query], convert_to_numpy=True)
        t0 = time.time()
        # 2. FAISS 检索
        distances, indices = self.index.search(query_vector, k)
        search_time = (time.time() - t0) * 1000  # 转毫秒

        results = []
        top_distance = distances[0][0]
        top_index = indices[0][0]

        # 3. 简单的结果封装
        if top_index != -1:  # 确保找到了结果
            # L2距离越小越相似。一般来说，e5模型下，
            # 距离 < 0.5 非常相似
            # 距离 > 1.2 可能就不太相关了 (需要根据实际体感微调阈值)
            matched_text = self.knowledge_text[top_index]
            return matched_text, top_distance, search_time

        return None, 999.0, search_time


